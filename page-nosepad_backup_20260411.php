<script>
/* Floating CTA: ヒーローを過ぎたら表示、buyセクションで非表示 */
(function() {
  var fcta = document.getElementById('floatingCta');
  var hero = document.querySelector('.hero');
  if (fcta && hero) {
    var buySection = document.getElementById('buy');
    var heroVisible = true;
    var buyVisible = false;

    function updateFcta() {
      if (!heroVisible && !buyVisible) {
        fcta.classList.add('visible');
      } else {
        fcta.classList.remove('visible');
      }
    }

    var heroObs = new IntersectionObserver(function(entries) {
      entries.forEach(function(e) { heroVisible = e.isIntersecting; updateFcta(); });
    }, { threshold: 0.1 });
    heroObs.observe(hero);

    if (buySection) {
      var buyObs = new IntersectionObserver(function(entries) {
        entries.forEach(function(e) { buyVisible = e.isIntersecting; updateFcta(); });
      }, { threshold: 0.1 });
      buyObs.observe(buySection);
    }
  }
})();

document.querySelectorAll('.faq-q').forEach(btn => {
  btn.addEventListener('click', () => {
    const item = btn.closest('.faq-item');
    const isOpen = item.classList.contains('open');
    document.querySelectorAll('.faq-item').forEach(i => i.classList.remove('open'));
    if (!isOpen) item.classList.add('open');
  });
});
const observer = new IntersectionObserver((entries) => {
  entries.forEach(e => {
    if (e.isIntersecting) { e.target.classList.add('in'); observer.unobserve(e.target); }
  });
}, { threshold: 0.12 });
document.querySelectorAll('.reveal').forEach(el => observer.observe(el));
</script>
